#include "calc.h"

double cplus(double x, double y) {
    return x + y;
}

double cbrokePlus(double x, double y) {
    return x + y + 1;
}

double cminus(double x, double y) {
    return x - y;
}

double cmultiply(double x, double y) {
    return x * y;
}

double cdivide(double x, double y) {
    return x / y;
}